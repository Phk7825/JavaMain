package day2;
// 비교연산자 : > <(크냐?작냐?)
//			>= 이상이냐? (NOT =>) 
//			<= 이하냐?  (NOT =<)
//			== 같냐?
//			!= 다르냐?
//	---> 연산의 결과, true 또는 false
public class Ex206 {
	public static void main(String[] args) {
		int a = 3;
		int b = 2;
		System.out.println(a>b);	// a가 b보다 크냐?
		System.out.println(a<=b); 	// a가 b 이하냐?
	}
}

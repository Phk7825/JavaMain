package day4;

//Q301,Q302 복습
public class Q401 {
	public static void main(String[] args) {
		System.out.println(0b100);	// 2진수 12
		System.out.println(014);	// 8진수 12
		System.out.println(0xC);	// 16진수 12
		System.out.println();		// 줄 바꿈 = "\n"
		
		// 7, 14, 21
		System.out.println(07);		// 8진수 7
		System.out.println(016);	// 8진수 14
		System.out.println(025 + "\n");		// 8진수 21 + 줄 바꿈
		
		// 7, 14, 21
		System.out.println(0x7);	// 2진수 7
		System.out.println(0xE);	// 2진수 14
		System.out.println(0x15);	// 2진수 21
		
	}
}
